
def seconds_to_time(n):
    # Витягування годин, хвилин та секунд з кількості секунд, що минули з початку доби
    hours = (n // 3600) % 24  # Загальна кількість годин, враховуючи добу
    minutes = (n % 3600) // 60  # Витягування хвилин
    seconds = n % 60  # Витягування секунд

    # Форматування результату з доповненням нулями, якщо потрібно
    print(f"{hours}:{minutes:02}:{seconds:02}")

n = 3602
seconds_to_time(n)


