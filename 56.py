def get_tens_digit(number):
    """
    Функція для обчислення цифри в розряді десятків числа.
    
    :param number: Натуральне число.
    :return: Цифра в розряді десятків.
    """
    # Отримуємо розряд десятків за допомогою цілочисельного ділення та залишку від ділення
    tens_digit = (number // 10) % 10
    return tens_digit

# Запитуємо користувача ввести натуральне число
number = int(input("Введіть натуральне число: "))

# Викликаємо функцію і отримуємо результат
tens_digit = get_tens_digit(number)

# Виводимо результат
print(f"Цифра в розряді десятків: {tens_digit}")
