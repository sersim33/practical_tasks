# Равлик повзе по вертикальній жердині висотою h метрів, піднімаючись за день на a метрів, 
# а за ніч спускаючись на b метрів. На який день равлик доповзе до вершини жердини? Дані вводяться у порядку h, a, b.

import math

def days_to_reach_top(h, a, b):
    if a >= h:
        return 1
    return 1 + math.ceil((h - a) / (a - b))

# Приклад використання:
h = int(input("Введіть висоту жердини h: "))
a = int(input("Введіть підйом за день a: "))
b = int(input("Введіть спуск за ніч b: "))

print(f"Равлик доповзе до вершини на {days_to_reach_top(h, a, b)} день.")


