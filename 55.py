def distribute_apples(n, k):
    """
    Функція для обчислення кількості яблук, які отримає кожен студент,
    і кількості яблук, що залишаться в кошику.
    
    :param n: Кількість студентів.
    :param k: Кількість яблук.
    :return: Кортеж з двох значень: кількість яблук на кожного студента і кількість яблук, що залишаться в кошику.
    """
    apples_per_student = k // n
    apples_left = k % n
    
    return apples_per_student, apples_left

# Запитуємо користувача ввести кількість студентів
n = int(input("Введіть кількість студентів: "))

# Запитуємо користувача ввести кількість яблук
k = int(input("Введіть кількість яблук: "))

# Викликаємо функцію і отримуємо результати
apples_per_student, apples_left = distribute_apples(n, k)

# Виводимо результати
print("______________________")
print(f"Кожен студент отримає {apples_per_student} яблук.")
print(f"В кошику залишиться {apples_left} яблук.")
